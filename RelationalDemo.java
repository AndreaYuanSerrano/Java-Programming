public class RelationalDemo {
	public static void main(String[] args) {
		
	  //local vairiables
		int x = 24;
		int y = 50;
		int z = -2;
		
		
	System.out.println("RELATIONAL OPERATORS");
		System.out.println( x + "==" + y + ":"+ (x==y));
		System.out.println( x + "!=" + y + ":"+ (x!=y));
		
		System.out.println( z + ">" + y + ":"+ (z>y));
		System.out.println( z + ">=" + y + ":"+ (z>y));
		
		System.out.println( z + "<" + x + ":"+ (z<x));
		System.out.println( z + "<=" + x + ":"+ (z<x));
		
	}
}